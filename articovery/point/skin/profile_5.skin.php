										나의 작업에서 가장 중요한 표현수단이자 주된 매체는 천(fabric)이다. ‘본다는 것’의 시각성에 관심을 가지며, 화면 안의 선, 면, 공간을 구성(composition)한다. 예술 작품이 꼭 이야기를 이루어야 한다고 생각하지 않는다. 개인 삶에서의 서사적 의미나 특정한 개념이 아닌, 각자의 규칙을 담아 자유로운 개념으로, 오로지 개인의 예술표현을 위한 소재로 천을 선택하였다. ‘페브릭 드로잉Fabric Drawing’은 말 그대로 천으로 그림을 그리는 회화다. 다시 말해, 천 표면의 질감, 색채, 패턴의 조형요소들로 새로운 형태를 창조하며, 회화 장르에서 새로운 시각효과와 표현 가능성을 모색한다.<br />
