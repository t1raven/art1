<script>
var bookFn = {
	align : function(){
		var vw = window.innerWidth || document.documentElement.clientWidth || document.body.clientWidth;
		var t = $("#flipbook");
		if(vw > 767){
			t.turn({page : 2,autoCenter: true}).addClass('double');
		}else{
			t.turn({page : 3,autoCenter: true, display : 'single'}).addClass('single');
		}
		
	},
	flip : function(dire){
		var t = $("#flipbook");
		if(dire == -1){
			if(t.turn("page") > 3){
				t.turn("previous");
			}
		}else{
			t.turn("next");
		}
	},
	resize : function(){
		setTimeout(function(){
			var vw = window.innerWidth || document.documentElement.clientWidth || document.body.clientWidth;
			var bx = $("#area_about .art1_book > .inner");
			var t = $("#flipbook");
			var h = bx.height();
			var w = bx.width();
			if(vw > 767){
				if(!t.hasClass('double')){
					t.turn("display", 'double').removeClass('single').addClass('double');
				}
			}else{
				if(!t.hasClass('single')){
					t.turn("display", 'single').removeClass('double').addClass('single');
				}
			}
			t.turn("size", w, h);
		},50);
	}
}
bookFn.align();
$(window).resize(bookFn.resize);
